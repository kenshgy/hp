function getValueByPath(source, path) {
  return path.split(".").reduce((value, key) => {
    if (value == null) {
      return undefined;
    }
    return value[key];
  }, source);
}

function renderSimpleCards(containerId, items, builder) {
  const container = document.getElementById(containerId);
  if (!container) {
    return;
  }

  container.innerHTML = items.map(builder).join("");
}

function updateTextContent(langContent) {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getValueByPath(langContent, element.dataset.i18n);
    if (typeof value === "string") {
      element.textContent = value;
    }
  });
}

function updateContactLinks(email) {
  document.querySelectorAll("[data-contact-link]").forEach((element) => {
    element.setAttribute("href", `mailto:${email}`);
    if (
      element.classList.contains("floating-cta") ||
      element.querySelector("[data-i18n]")
    ) {
      return;
    }
    if (element.textContent.includes("@") || element.closest(".contact-email")) {
      element.textContent = email;
    }
  });
}

function updateLanguage(lang) {
  const langContent = content[lang] ?? content.ja;
  document.documentElement.lang = lang;
  document.title = langContent.meta.title;

  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription) {
    metaDescription.setAttribute("content", langContent.meta.description);
  }

  updateTextContent(langContent);
  updateContactLinks(langContent.meta.contactEmail);

  renderSimpleCards("trust-items", langContent.trust.items, (item) => {
    return `
      <article class="trust-item">
        <h3>${item.title}</h3>
        <p>${item.body}</p>
      </article>
    `;
  });

  renderSimpleCards("about-points", langContent.about.points, (item) => {
    return `
      <article class="about-point">
        <h3>${item.title}</h3>
        <p>${item.body}</p>
      </article>
    `;
  });

  renderSimpleCards("services-list", langContent.services.items, (item) => {
    return `
      <article class="service-card">
        <span class="card-tag">${item.tag}</span>
        <h3>${item.title}</h3>
        <p>${item.body}</p>
      </article>
    `;
  });

  renderSimpleCards("process-list", langContent.process.items, (item) => {
    return `
      <article class="process-card">
        <span class="step-index">${item.step}</span>
        <h3>${item.title}</h3>
        <p>${item.body}</p>
      </article>
    `;
  });

  renderSimpleCards("strengths-list", langContent.strengths.items, (item) => {
    return `
      <article class="strength-card">
        <h3>${item.title}</h3>
        <p>${item.body}</p>
      </article>
    `;
  });

  renderSimpleCards("cases-list", langContent.cases.items, (item) => {
    return `
      <article class="case-card">
        <span class="case-kicker">${item.kicker}</span>
        <h3>${item.title}</h3>
        <dl class="case-meta">
          <div>
            <dt>${lang === "ja" ? "課題" : "Challenge"}</dt>
            <dd>${item.challenge}</dd>
          </div>
          <div>
            <dt>${lang === "ja" ? "支援" : "Support"}</dt>
            <dd>${item.support}</dd>
          </div>
          <div>
            <dt>${lang === "ja" ? "成果" : "Outcome"}</dt>
            <dd>${item.outcome}</dd>
          </div>
        </dl>
      </article>
    `;
  });

  document.querySelectorAll("[data-lang-toggle]").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.langToggle === lang);
  });
}

function closeNav() {
  document.body.classList.remove("is-nav-open");
  const toggle = document.querySelector("[data-nav-toggle]");
  if (toggle) {
    toggle.setAttribute("aria-expanded", "false");
  }
}

function toggleNav() {
  const isOpen = document.body.classList.toggle("is-nav-open");
  const toggle = document.querySelector("[data-nav-toggle]");
  if (toggle) {
    toggle.setAttribute("aria-expanded", String(isOpen));
  }
}

document.querySelectorAll("[data-lang-toggle]").forEach((button) => {
  button.addEventListener("click", () => {
    updateLanguage(button.dataset.langToggle);
    closeNav();
  });
});

const navToggle = document.querySelector("[data-nav-toggle]");
if (navToggle) {
  navToggle.addEventListener("click", toggleNav);
}

document.querySelectorAll("[data-nav-link]").forEach((link) => {
  link.addEventListener("click", closeNav);
});

window.addEventListener("resize", () => {
  if (window.innerWidth > 840) {
    closeNav();
  }
});

updateLanguage("ja");
